	/**
		Error display function for debugging purposes.
	*/
	function error(msg)
	{
		var caller = arguments.caller;
		while (caller != null)
		{
			var str = caller.callee.toString(); // The whole function as a string.
			var index = str.indexOf("\n"); // Try LF.
			if (index == -1) index = str.indexOf("\r"); // Try CR.
			if (index != -1)
			{
				str = str.substring(0, index); // Get the first line only.
			}
			msg += "\n- " + str;
			caller = caller.caller;
		}
		alert(msg);
	}
