	{
		if (currentPropertyIndex < propertyNames.length - 1)
		{
			currentPropertyIndex++;
			if (currentPropertyIndex > topmostPropertyIndex)
			{
				topmostPropertyIndex = currentPropertyIndex;
			}
			return true;
		}
		else
		{
			topmostPropertyIndex = propertyNames.length;
			return false; // Cannot increment, no more properties.
		}
	}
