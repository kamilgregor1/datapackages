	/**
		Sorts the specified array and the accompanying index array.
	*/
	function sort(aValues, aIndexes, reverse)
	{
		var count = aValues.length;
		assert( count == aIndexes.length );
		count--; // Partial out the (count - 1) subexpression.
		for (var i = 0; i < count; i++)
		{
			for (var j = 0; j < count - i; j++)
			{
				if (reverse)
				{
					if (aValues[j] < aValues[j+1])
					{
						var temp = aValues[j];
						aValues[j] = aValues[j+1];
						aValues[j + 1] = temp;
						
						// Swap the accompanying index array.
						temp = aIndexes[j];
						aIndexes[j] = aIndexes[j+1];
						aIndexes[j+1] = temp;
					}
				}
				else
				{
					if (aValues[j] > aValues[j+1])
					{
						var temp = aValues[j];
						aValues[j] = aValues[j+1];
						aValues[j + 1] = temp;
						
						// Swap the accompanying index array.
						temp = aIndexes[j];
						aIndexes[j] = aIndexes[j+1];
						aIndexes[j+1] = temp;
					}
				}
			}
		}
	}
