	/**
		Performs a recalculation of the result.
	*/
	function recalc()
	{
		// Set the values of the index array.
		for (var i = 0; i < getObjectCount(); i++)
		{
			distanceObjects[i] = i;
		}
		
		// Check if bogus.
		
		if (bogus) return; // --> Quit if allready bogus.
		
		bogus = false;
		var debugStr = "";
		
		// Hide locals in a separate scope.
		{
			var time = new Date() - startTime;
			if (time < 45)
			{
				bogus = true; // --> Bogus if it took less than 45sec.
				debugStr += "Bogus (time)\n";
			}
			var count = 0;
			var similarSubjectValues = true;
			var previousSubjectValue = getPropertyValue(0);
			
			for (var i = 0; i < getPropertyCount(); i++)
			{
				var subjectValue = getPropertyValue(i);
				if (similarSubjectValues) // Check if this answer is different from the previous.
				{
					similarSubjectValues = (subjectValue == previousSubjectValue);
				}
				if (subjectValue != null) // Skipped values are not taken into account.
				{
					count++;
				}
			}
			var selectedCount = 0;
			for (var i = 0; i < getObjectCount(); i++)
			{
				if (isObjectSelected(i)) selectedCount ++;
			}
			
			if (similarSubjectValues)
			{
				bogus = true;
				debugStr += "Bogus (variance)\n";
			}
			if (count <= 5)
			{
				bogus = true;
				debugStr += "Bogus (count)\n";
			}
			if (selectedCount < 2)
			{
				bogus = true;
				debugStr += "Bogus (selected)\n";
			}
			if (debug) bogus = false; // Continue as if normal in debug mode.
		}
		if (bogus)
		{
			for (var i = 0; i < getObjectCount(); i++)
			{
				distanceValues[i] = null;
			}
			return; // --> recalcRequired stays true!
		}
		// Compute maxDistance.
		// MaxDistnace kan weg voor Cohen K?		
		var count = 0;
		var totalDistance = 0;
		for (var i = 0; i < propertyValues.length; i++)
		{
			var subjectValue = propertyValues[i];
			// Skipped values are not taken into account.
			if (subjectValue != null)
			{
				// Max distance == 2 when the weight == 2.
				// Max distance == 1 when the weight == 1
				// TODO: This should be in sync with the algorithm in computeDistance().
				// var max = 2 * getPropertyGroupWeight(propertyToGroupMapping[i]);
				var max = getPropertyGroupWeight(propertyToGroupMapping[i]);
				totalDistance += max; // Abs not needed since maxDistance is allways positive.
				count++;
			}
		}
		var maxDistance = totalDistance;
		if (debug) debugStr += "D\tD/Max\tScaled\n0\t0\t100\tMin\n" +
			Math.round(100 * maxDistance) + "\t1\t-100\tMax\n";
		
		//
		// Method Cohen's Kappa				
		//
		var indexEens = 2;
		var indexWeetNiet = 1;
		var indexOneens = 0;
		var answersIndexed = new Array (indexEens, indexWeetNiet, indexOneens);

		//
		// /CK
				
		
		// Compute raw distances to all objects.
		for (var objectIndex = 0; objectIndex < getObjectCount(); objectIndex++)
		{
			if (isObjectSelected(objectIndex))
			{
				// Walk along all objects (= parties).
				var lObjectPropValues = objectPropertyValues[objectIndex];
				assert(lObjectPropValues.length == propertyValues.length);
				count = 0;
				totalDistance = 0;

				var subjectAnswerCount = new Array(0,0,0);
				var objectAnswerCount = new Array(0,0,0);
				var expectedDistance = 0;   								// a
				var propertyCount = propertyValues.length;					// c is aantal stellingen	
				var kappa = 0;
		
				
				for (var i = 0; i < propertyValues.length; i++)
				{
					var objectValue = lObjectPropValues[i]; // Answer of the party.
					var subjectValue = propertyValues[i];
					var subjectWeight = getPropertyGroupWeight(propertyToGroupMapping[i]);
					var distance = computeDistance(objectValue, subjectValue, subjectWeight);  
					// distance for Ck is 0, 1 or 2 when weighted
					
					//
					// Cohen Kappa
					//
					assert( in_array(answersIndexed, subjectValue+1) );
					assert( in_array(answersIndexed, objectValue+1) );
					subjectAnswerCount[subjectValue+1] += subjectWeight;
					objectAnswerCount[objectValue+1] += subjectWeight;
					if (subjectWeight == 2) {
						propertyCount++; // extra weight counts as extra answer.
					}
					
					//
					// /CK
					
					if (distance != null)
					{
						totalDistance += distance;
						count++;
					}
				}
				
				//
				// Cohen Kappa
				//
				var actualDistance = totalDistance;   							// b 

				
				for (var i = 0; i < answersIndexed.length; i++)
				{
				expectedDistance += ( (subjectAnswerCount[i] * objectAnswerCount[i] ) / propertyCount );

				}
				kappa = ( actualDistance - expectedDistance) / ( propertyCount - expectedDistance);
				distanceValues[objectIndex] = kappa;
				//
				// /CK

				//see above, dV=Kappa   distanceValues[objectIndex] = totalDistance;
				
			}
			else // Not selected
			{
				distanceValues[objectIndex] = Number.NaN;
			}
		}
		
		// Normalize.
		// var maxDistance = Number.MIN_VALUE;
		
		var minDistance = 0;
		for (var objectIndex = 0; objectIndex < getObjectCount(); objectIndex++)
		{
			if (isObjectSelected(objectIndex))
			{
				var distance = distanceValues[objectIndex];
				if (distance > maxDistance) maxDistance = distance; 
				if (distance < minDistance) minDistance = distance; 
			}
		}
		// Show: Distance
		if (debug) debugStr += "min:\t" + minDistance + "\tscaled1 = (d-min)/(max-min) * 100\n" +
			"max:\t" + maxDistance + "\tscaled2 = -scaled1 * 2 - 100\n" +
			"max-min:\t" + (maxDistance - minDistance) + "\n\n" +
			"d\td-min\tscaled1\tscaled2\n";

		// Sort the distances and the accompanying index array.
//		sort(distanceValues, distanceObjects, true);
		var count = distanceValues.length - 1;
		for (var i = 0; i < count; i++)
		{
			for (var j = 0; j < count - i; j++)
			{
				var distance = distanceValues[j];
				if (isNaN(distance) || distance < distanceValues[j + 1])
				{
					var temp = distanceValues[j];
					distanceValues[j] = distanceValues[j +  1];
					distanceValues[j + 1] = temp;
					
					// Swap the accompanying index array.
					temp = distanceObjects[j];
					distanceObjects[j] = distanceObjects[j +  1];
					distanceObjects[j + 1] = temp;
				}
			}
		}
		
		// Are there any duplicates? If so take one randomly.
		{
			var lTopDist = distanceValues[0];
			var lTopCount = 1;
			var index = 1;
			while (index < count && distanceValues[index] == lTopDist)
			{
				lTopCount++;
				index++;
			}
			
			if (lTopCount > 1)
			{
				// Select a random number 0 <= R < lTopCount.
				var index = Math.floor(Math.random() * lTopCount);
				assert(0 <= index && index < lTopCount);
				// alert("Selected " + index + " from " + lTopCount + " at the top");
				if (index > 0)
				{
					var temp = distanceValues[index];
					distanceValues[index] = distanceValues[0];
					distanceValues[0] = temp;
					
					temp = distanceObjects[index];
					distanceObjects[index] = distanceObjects[0];
					distanceObjects[0] = temp;
				}
			}
		}
		
		if (debug)
		{
			debugStr += "\n";
			for (var i = 0; i < distanceValues.length; i++)
			{
				debugStr += "\n" + Math.round(100 * distanceValues[i]) + "\t" + getObjectDesc(distanceObjects[i]);
			}
			alert(debugStr);
		}
		
		// Show Enquete button randomly one in every oneIn
		var oneIn = 4; 
		showEnquete = false;   // Math.random() < ( 1 / oneIn ) ? true : false ;

		recalcRequired = false;
		setResultDetailObject(getDistanceObjectIndex(0));
	}
