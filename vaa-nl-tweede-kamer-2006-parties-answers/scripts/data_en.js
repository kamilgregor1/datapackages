	/**
		Emits the result.
	*/
	function emitResult(wnd)
	{
		var markup = '';
		window.onbeforeunload = null; // Vanaf dit moment geen waarschuwing meer bij afsluiten.

		if (isBogus())
		{
				markup += '<p>You have given an insufficient number of serious answers, no result will be given.</p>';
		if (!isStatUpdated()) // Update stat if necessary.
			{
				var data = appID + "'" 
				var url = "/swstats?" + data;
				frames[1].location.replace( url );
				setStatsUpdated(); // Refresh nodig voor opnieuw zenden (SdB).
			}
		}
		else
		{
			var objectIndex = getDistanceObjectIndex(0);
		
			markup += '<div id="stemadvies">';


			var lObjectImage = getObjectImage(objectIndex);		
			if (lObjectImage.length != 0)
			{
				markup += '<img src="../' + lObjectImage + '" alt="partijlogo" />';
			}				
			// Normale SW
			markup += '<h1>Voting recommendation</h1><p>' + 
			getObjectDesc(objectIndex) + '</p></div>';

			if (!isStatUpdated()) // Update stat if necessary.
			{
				// All properties should have been visited at this point!
				assert(allPropertiesVisited());
				// Write to the IO frame that calls the update servlet.
				// - The appID.
				// - The objectID 'StemAdvies (partij ID)'.
				// - All responses.
			 	// Responses have the following pattern: [PropertyID][.][PropertyAnswerValue]
				// e.g. "25.-1'26.0'27.1'28.0" etc. Nulls are encoded as empty strings.
				var index = getDistanceObjectIndex(0);
				var objectID = getObjectID(index);
				var data = appID + "'" + objectID + "'";
				for (var i = 0; i < getPropertyCount(); i++)
				{
					var value = getPropertyValue(i);
					data += getPropertyID(i) + "." + (value == null ? '' : value) + "'";
				}
				// Append PropertyGroupWeights.
				for (var i = 0; i < getPropertyGroupCount(); i++)
				{
					var weight = getPropertyGroupWeight(i);
					if (weight > 1)
					{
						var desc = getPropertyGroupDesc(i);
						// Length prefixed instead of a separator to prevent clashes.
						data += escape( String.fromCharCode( desc.length ) + desc );
					}
				}
				var url = "/swstats?" + data;
				frames[1].location.replace( url );
				setStatsUpdated();
			}
		}

		if (showEnquete)
		{
			var enqButton = wnd.document.getElementById('enquete-banner');
			enqButton.style.display = "block";
		}
		

		wnd.document.write(markup);
	}
