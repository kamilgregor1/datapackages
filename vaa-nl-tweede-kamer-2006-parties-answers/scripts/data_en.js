	/**
		Sets the object stats. Arguments are: objectID0, count0, objectID1, count1, etc..
	*/
	function setObjectStats()
	{
		var args = arguments;
		for (var i = 0; i < args.length; i+=2) // Increment by 2!
		{
			var objectID = args[i];
			var count = args[i+1];
			var index = getObjectIndex(objectID);
			if (index != null) // Lenience towards unknown objectIDs.
			{
				objectStatValues[index] = count;
			}
		}
	}
